package com.labinot.bajrami.social_app.models;

public class Comment {

    private String id;
    private String comment;
    private String publisher;

    public Comment() {
    }


    public String getId() {
        return id;
    }

    public String getComment() {
        return comment;
    }

    public String getPublisher() {
        return publisher;
    }
}
